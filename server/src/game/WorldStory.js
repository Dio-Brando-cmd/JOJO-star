/**
 * 获取某角色的完整背景故事
 */
export function getCharacterStory(characterId) {
  return WORLD_STORY.characterStories[characterId] || null;
}

/**
 * 获取某角色的台词
 */
export function getCharacterQuote(characterId, type) {
  return WORLD_STORY.characterQuotes[characterId]?.[type] || null;
}

