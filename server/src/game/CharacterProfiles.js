/**
 * 获取某角色的完整档案
 */
export function getCharacterProfile(characterId) {
  return CHARACTER_PROFILES[characterId] || null;
}

/**
 * 获取某角色+某职业的组合描述
 */
export function getRoleManifestation(characterId, hiddenRole) {
  const profile = CHARACTER_PROFILES[characterId];
  if (!profile) return null;
  return profile.hiddenRoleManifestations[hiddenRole] || null;
}

/**
 * 获取角色的灵格等级
 */
export function getSpiritGrade(characterId) {
  const profile = CHARACTER_PROFILES[characterId];
  return profile?.spiritGrade || 'MORTAL';
}
